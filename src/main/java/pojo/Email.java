package pojo;

public class Email {
    String Code;
    String Email;
    int id;

    public String getCode() {
        return Code;
    }

    public String getEmail() {
        return Email;
    }

    public int getId() {
        return id;
    }

    public void setCode(String code) {
        Code = code;
    }

    public void setEmail(String email) {
        Email = email;
    }

    public void setId(int id) {
        this.id = id;
    }
}
